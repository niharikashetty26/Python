text = 'Niharika'

# upper() function to convert
print("\nConverted to upper case String:")
print(text.upper())

# lower() function to convert
print("\nConverted to lower case String:")
print(text.lower())

# converts the first character to upper case and rest to lower case
print("\nConverted to first char cap String:")
print(text.title())

# swaps the case of all characters in the string upper case character to lowercase and viceversa
print("\nConverted to all swap char of  String:")
print(text.swapcase())

# convert the first character of a string to uppercase
print("\nConverted first char of String to uppercase:")
print(text.capitalize())

